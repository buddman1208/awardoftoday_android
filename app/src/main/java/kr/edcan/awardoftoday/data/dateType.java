public class dateType {
    public String year;
    public String month;
    public String day;
    public String hour;
    public String minute;
    public dateType(String year, String month, String day, String hour, String minute){
        this.year = year;
        this.month = month;
        this.day = day;
        this.hour = hour;
        this.minute = minute;
    }
}
